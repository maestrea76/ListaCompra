const UI: Record<Locale, Dict> = { es: ES, en: EN, us: US, fr: FR, de: DE, br: BR };

/** Traduce una clave al locale dado. `{var}` se sustituye por `vars.var`. */
export function translate(
  locale: Locale | undefined,
  key: UIKey,
  vars?: Record<string, string | number>,
): string {
  const dict = UI[locale ?? DEFAULT_LOCALE] ?? UI[DEFAULT_LOCALE];
  let out: string = dict[key] ?? UI[DEFAULT_LOCALE][key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) out = out.split(`{${k}}`).join(String(v));
  }
  return out;
}
